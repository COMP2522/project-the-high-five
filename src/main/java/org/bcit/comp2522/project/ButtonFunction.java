package org.bcit.comp2522.project;

/**
 * An enumeration of the possible functions that a button can have, including starting a new game, loading a saved game,
 * viewing the high scores and quitting the application.
 */
public enum ButtonFunction {
    /** Represents the "start game" function of a button.*/
    START,
    /** Represents the "load saved game" function of a button.*/
    LOAD,
    /** Represents the "view high scores" function of a button.*/
    HIGHSCORE,
    /** Represents the "quit game" function of a button.*/
    QUIT
}
